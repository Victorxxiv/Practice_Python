# Basic arithmetic
a = 10
b = 3
print(a + b)  # Output: 13
print(a * b)  # Output: 30

# Conversion
s = "42"
n = int(s)
print(n)  # Output: 42

# Representations
print(bin(10))  # Output: '0b1010'
print(oct(10))  # Output: '0o12'
print(hex(10))  # Output: '0xa'

# Bit length
n = 7
print(n.bit_length())  # Output: 3 (binary representation is '111')